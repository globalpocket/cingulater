@pytest.mark.asyncio
async def test_run_workflow_with_reflection(orchestrator):
    """Restored: Test reflection (reranking) when no tool call is generated by LLM."""
    
    async def mock_stream_chat(*args, **kwargs):
        yield StandardLLMChunk(content="Hello World")
        yield StandardLLMChunk(finish_reason="stop")
        
    orchestrator.llm_client.stream_chat = mock_stream_chat
    orchestrator._extract_intent = AsyncMock(return_value="Conclude task")
    
    # Reranker モックの準備
    mock_reranker = AsyncMock()
    mock_reranker.call_tool.return_value = json.dumps([{"document": "Concludes the interaction", "score": 0.99}])
    orchestrator.mcp_clients["mcp-reranker"] = mock_reranker

    req = InternalAgentRequest(
        messages=[InternalMessage(role="user", content="Hi")],
        tools=[InternalTool(
            type="function",
            function={
                "name": "custom_finish_tool",
                "description": "Concludes the interaction",
                "parameters": {
                    "properties": {"summary": {"type": "string"}, "is_done": {"type": "boolean"}},
                    "required": ["summary", "is_done"]
                }
            }
        )]
    )

    events = [e async for e in orchestrator.process_workflow(req)]
    
    # TextDelta + SystemToolCall + WorkflowFinish
    assert any(isinstance(e, SystemToolCallEvent) and e.tool_name == "custom_finish_tool" for e in events)
    assert any(isinstance(e, WorkflowFinishEvent) for e in events)

